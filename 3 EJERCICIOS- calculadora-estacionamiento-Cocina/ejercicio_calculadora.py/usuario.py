class Usuario:
    def __init__ (self):
        self.nombre=""
        self.cedula=""
    
    def get_nombre(self):
        return self.nombre
    
    def set_nombre(self,nuevo_nombre):
        self.nombre=nuevo_nombre
    


    def get_cedula(self):
        return self.cedula
    
    def set_cedula(self,nueva_cedula):
        self.cedula=nueva_cedula

        
    

