# ========= Clase Usuario =========
class Usuario:
    def __init__(self, cedula, nombre, tipo):
        self.cedula = cedula
        self.nombre = nombre
        self.tipo = tipo


# ========= Clase Carro =========
class Carro:
    def __init__(self, placa, tipo, color):
        self.placa = placa
        self.tipo = tipo
        self.color = color






